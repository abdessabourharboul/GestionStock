    private void initClientCombobox() throws Exception {
        clientBox.removeAllItems();
        clientBox.addItem("--SELECT CLIENT--");
        List<Client> clients = clientService.findAll();
        for (Client article : clients) {
            clientBox.addItem(article.getId());
        }
        
    }
    
    private void initArticleCombobox() throws Exception {
        articleBox.removeAllItems();
        articleBox.addItem("--SELECT FOURNISSEURS--");
        List<Article> articles = articleService.findAll();
        for (Article article : articles) {
            articleBox.addItem(article.getId());
        }
        
    }
    
    private void initView() throws Exception {
        initClientCombobox();
        initArticleCombobox();
        initJtable();
    }

