    public Commande getParams() {
        Commande myCommande = new Commande();
        myCommande.setDateLivraison(((JTextField) dateLivraisontxt.getDateEditor().getUiComponent()).getText());
        myCommande.setManiereLivraison(maniereLivraison.getText());
        myCommande.getClient().setId(clientBox.getSelectedItem() + "");
        myCommande.setEtat("");
        myCommande.setLigneCommandes(new ArrayList<LigneCommande>());
        return myCommande;
    }

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Commande myCommande = getParams();
        System.out.println(myCommande.getId());
        try {
            int res = commandeService.save(myCommande);
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC SUCCES", "info", JOptionPane.INFORMATION_MESSAGE);
                jPanel2.setVisible(true);
                rescomID = myCommande.getId();
            } else {
                JOptionPane.showMessageDialog(null, "LA CONFIRMATION DU MOT DE PASSE EST INCORECTTE", "ereur", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(CommandeCreation.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC ERROR", "ereur", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

