    public LigneCommande getParamsLigneCommande() {
        LigneCommande myLigneCommande = new LigneCommande();
        myLigneCommande.setPrixVente(new Long(prixtxt.getText()));
        myLigneCommande.setQuantite(new Long(quantitetxt.getText()));
        myLigneCommande.getArticle().setId(articleBox.getSelectedItem() + "");
        myLigneCommande.getCommande().getLigneCommandes().add(myLigneCommande);
        myLigneCommande.getCommande().setId(rescomID);
        return myLigneCommande;
    }

