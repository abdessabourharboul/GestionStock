    public Sortie getParamSortie() {
        Sortie mySortie = new Sortie();
        mySortie.getArticle().setId(articleBox.getSelectedItem() + "");
        mySortie.setQuantite(new Long(quantitetxt.getText()));
        mySortie.setPrixVente(new Long(prixtxt.getText()));
//        mySortie.setDateSortie(datetxt.getText());
        mySortie.setDateSortie(((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText());
        mySortie.setEmploye(employetxt.getText());
        return mySortie;
    }

