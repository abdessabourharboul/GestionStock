    public String getSelectedButtonText(ButtonGroup buttonGroup) {
        for (Enumeration<AbstractButton> buttons = buttonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                return button.getText();
            }
        }
        return null;
    }


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Sortie mySortie = getParamSortie();
        Entree myEntree = getParamEntree();
        String selectedButton = getSelectedButtonText(buttonGroup1);
        if (selectedButton.equals("Entrer")) {
            System.out.println(selectedButton);
            System.out.println(myEntree.toString());
            try {
                Article loadedArticle = articleService.findById(mySortie.getArticle().getId());
                int res = entreeService.save(myEntree);
                loadedArticle.setQuantite(loadedArticle.getQuantite() + myEntree.getQuantite());
                articleService.update(loadedArticle);

                if (res > 0) {
                    JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC SUCCES", "info", JOptionPane.INFORMATION_MESSAGE);

                    this.setVisible(false);
                    new Accueil().setVisible(true);

                } else {
                    JOptionPane.showMessageDialog(null, "LA CONFIRMATION DU MOT DE PASSE EST INCORECTTE", "ereur", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC ERROR", "ereur", JOptionPane.ERROR_MESSAGE);
            }

        } else if (selectedButton.equals("Sortir")) {
            System.out.println(selectedButton);
            System.out.println(mySortie.toString());

            try {
                Article loadedArticle = articleService.findById(mySortie.getArticle().getId());
                if (mySortie.getQuantite() > loadedArticle.getQuantite()) {
                    JOptionPane.showMessageDialog(null, "La quantite demandee est superieur a celle du stock", "ereur", JOptionPane.ERROR_MESSAGE);
                } else if (loadedArticle.getQuantite() < 5) {
                    JOptionPane.showMessageDialog(null, "Il n'y a pas suffisament dans le stock", "ereur", JOptionPane.ERROR_MESSAGE);

                } else {
                    int res = sortieService.save(mySortie);
                    loadedArticle.setQuantite(loadedArticle.getQuantite() - mySortie.getQuantite());
                    articleService.update(loadedArticle);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC SUCCES", "info", JOptionPane.INFORMATION_MESSAGE);
                        this.setVisible(false);
                        new Accueil().setVisible(true);

                    } else {
                        JOptionPane.showMessageDialog(null, "LA CONFIRMATION DU MOT DE PASSE EST INCORECTTE", "ereur", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "INSCRIPTION AVEC ERROR", "ereur", JOptionPane.ERROR_MESSAGE);

            }

        }


    }//GEN-LAST:event_jButton2ActionPerformed

